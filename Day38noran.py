#Python Arrays2
#Looping Array Elements
colors = ["Blue", "White", "Black", "Red"]
for z in colors:
    print(z)
#Adding Array Elements used append()
Languages = ["Javascript", "PHP", "Python"]
Languages.append("Microbit")
print(Languages)
#Removing Array Elements used pop() or remove()
fruits = ["Blueberry", "cherry", "blackberry", "rasepberry"]
fruits.pop(1)
print(fruits)
fruits.remove("blackberry")
print(fruits)